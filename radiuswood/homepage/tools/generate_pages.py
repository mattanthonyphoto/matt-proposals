def render_photos(photos):
    """photos: list of (src, caption) OR (src, caption, href) tuples."""
    out = []
    for p in photos:
        if len(p) == 3:
            src, cap, href = p
            out.append(f'<a href="{href}"><figure><img loading="lazy" src="{src}" alt="{cap}"><figcaption>{cap}</figcaption></figure></a>')
        else:
            src, cap = p
            out.append(f'<figure><img loading="lazy" src="{src}" alt="{cap}"><figcaption>{cap}</figcaption></figure>')
    return "\n  ".join(out)


def render_schema(page):
    extra = page.get("schema_extra")
    if not extra:
        return ""
    locality = extra["locality"]
    year_line = f',\n  "dateCreated": "{extra["year"]}"' if extra.get("year") else ""
    desc = extra["desc"]
    image = extra["image"]
    name = extra["name"]
    return f"""<script type="application/ld+json">
{{
  "@context": "https://schema.org",
  "@type": "CreativeWork",
  "name": "{name}",
  "creator": {{"@type": "Organization", "name": "Radius Architectural Millwork Ltd"}},
  "locationCreated": {{"@type": "Place", "address": {{"@type": "PostalAddress", "addressLocality": "{locality}", "addressRegion": "BC", "addressCountry": "CA"}}}}{year_line},
  "image": "{image}",
  "description": "{desc}"
}}
</script>"""


