# Page data
# ────────────────────────────────────────────────────────────────────────────
# Per-page fields:
#   out:          output path relative to homepage/
#   prefix:       relative URL prefix used in nav (e.g., "../" for one-level-deep)
#   title, desc, canonical
#   crumbs:       list of (label, href) tuples for breadcrumb; last has href=None
#   hero_img:     hero banner image path (relative to page)
#   eyebrow, h1:  hero overlay text (h1 supports <em>)
#   copy_eye, copy_h2, copy_p:  copy block
#   photos:       list of (src, caption) tuples
#   cta_eye, cta_h2, cta_p, cta_btn_text, cta_btn_href, cta_bg
#   schema:       optional schema.org JSON-LD (string, full <script> block) or None

